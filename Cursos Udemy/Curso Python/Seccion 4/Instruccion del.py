# La instruccion del es para borrar items o para borrar por completo

a = ['a', 'b', 'c', 'd', 'e']

del a[2] # Borrar la posicion 2 'c' en la lista a

del a[:2] # Borrar desde el primer elemento hasta el elemento 2

del a[2:] # Borrar desde el elemento 2 hasta el ultimo

del a # Borrar la lista

print(a)